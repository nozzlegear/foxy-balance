    [<Fact>]
    member _.``MatchTransactionToBill should mark auto-generated transaction as cleared``() =
        task {
            // Setup
            let! user = createUser ()

            let! bill = createBill user.Id "Rent" 1200M FirstWeek DayOfWeek.Monday
            let targetDate = DateTimeOffset(2024, 1, 1, 0, 0, 0, TimeSpan.Zero)

            // Create an auto-generated pending transaction from the bill
            let autoGenTransaction: PartialTransaction =
                { Name = "Rent"
                  Amount = 1200M
                  DateCreated = targetDate
                  Status = Pending
                  Type = Debit
                  ImportId = None
                  RecurringBillId = Some bill.Id
                  AutoGenerated = true }
            let! autoGen = transactionDatabase.CreateAsync(user.Id, autoGenTransaction)

            // Create an imported transaction
            let! imported = createTransaction user.Id "LANDLORD RENT PAYMENT" 1200M (targetDate.AddDays(1.0))

            // Act
            let! result = service.MatchTransactionToBill(user.Id, imported.Id, bill.Id)

            // Assert
            %result.Should().BeOk()

            // Check that auto-generated transaction is now cleared
            let! autoGenUpdated = transactionDatabase.GetAsync(user.Id, autoGen.Id)
            %autoGenUpdated.Should().BeSome()

            match autoGenUpdated.Value.Status with
            | Cleared clearedDate ->
                %clearedDate.Should().BeCloseTo(imported.DateCreated, TimeSpan.FromSeconds 1L)
            | Pending ->
                failwith "Auto-generated transaction should be cleared"
        }

