    [<Fact>]
    member _.``History heuristic should boost score when transaction name was previously matched to bill``() =
        task {
            // Setup
            let! user = createUser ()
            let amount = 108M
            let! bill = createBill user.Id "Internet" amount FirstWeek DayOfWeek.Monday
            let targetDate = calculateExpectedDate FirstWeek DayOfWeek.Monday

            // Seed one prior confirmed match with the same transaction name
            let historicalTransaction: PartialTransaction =
                { Name = "Withdrawal from Premier Comm Telecommun"
                  Amount = amount
                  DateCreated = targetDate.AddMonths(-1)
                  Status = Cleared (targetDate.AddMonths(-1))
                  Type = Debit
                  ImportId = Some (Guid.NewGuid().ToString())
                  RecurringBillId = Some bill.Id
                  AutoGenerated = false }
            let! _ = transactionDatabase.CreateAsync(user.Id, historicalTransaction)

            // Two new candidates: one whose name matches history, one that doesn't
            let! withHistory = createTransaction user.Id "Withdrawal from Premier Comm Telecommun" amount targetDate
            let! withoutHistory = createTransaction user.Id "ACH DEBIT PAYMENT" amount targetDate

            // Act
            let! matches = service.GetMatchSuggestionsForUser(user.Id)

            // Assert
            let matchesList = matches |> Seq.toList
            let withHistoryScore =
                matchesList |> List.find (fun m -> m.Transaction.Id = withHistory.Id && m.RecurringBill.Id = bill.Id) |> _.MatchScore
            let withoutHistoryScore =
                matchesList |> List.find (fun m -> m.Transaction.Id = withoutHistory.Id && m.RecurringBill.Id = bill.Id) |> _.MatchScore

            // 1 prior match -> historyScore = 50 -> contribution = 50 * 0.15 = 7.5 points
            %withHistoryScore.Should().BeGreaterThan(withoutHistoryScore)
            %(withHistoryScore - withoutHistoryScore).Should().BeCloseTo(7.5M, 0.5M)
        }

    [<Fact>]
