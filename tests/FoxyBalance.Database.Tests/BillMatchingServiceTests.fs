    [<Fact>]
    member _.``GetMatchSuggestionsForUser should only show bills for the specific user``() =
        task {
            // Setup
            let! user1 = createUser ()
            let! user2 = createUser ()

            // Create bills for both users
            let! user1Bill = createBill user1.Id "User1 Bill" 100M FirstWeek DayOfWeek.Monday
            let! user2Bill = createBill user2.Id "User2 Bill" 100M FirstWeek DayOfWeek.Monday

            let targetDate = calculateExpectedDate FirstWeek DayOfWeek.Monday

            // Create transactions for both users
            let! user1Transaction = createTransaction user1.Id "Payment 1" 100M targetDate
            let! user2Transaction = createTransaction user2.Id "Payment 2" 100M targetDate

            // Act
            let! user1Matches = service.GetMatchSuggestionsForUser(user1.Id)
            let! user2Matches = service.GetMatchSuggestionsForUser(user2.Id)

            // Assert
            let user1MatchesList = user1Matches |> Seq.toList
            let user2MatchesList = user2Matches |> Seq.toList

            // User1 should only see their own bill and transaction
            %user1MatchesList.Should().AllSatisfy(fun m ->
                %m.Transaction.Id.Should().Be(user1Transaction.Id)
                %m.RecurringBill.Id.Should().Be(user1Bill.Id))

            // User2 should only see their own bill and transaction
            %user2MatchesList.Should().AllSatisfy(fun m ->
                %m.Transaction.Id.Should().Be(user2Transaction.Id)
                %m.RecurringBill.Id.Should().Be(user2Bill.Id))
        }

