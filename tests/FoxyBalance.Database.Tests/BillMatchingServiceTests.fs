[<Collection("BillMatchingService")>]
type BillMatchingServiceTests(fixture: DbContainerFixture) =
    let billDatabase: IRecurringBillDatabase = RecurringBillDatabase(TestDatabaseOptions fixture)
    let transactionDatabase: ITransactionDatabase = TransactionDatabase(TestDatabaseOptions fixture)
    let userDatabase: IUserDatabase = UserDatabase(TestDatabaseOptions fixture)
    let service = BillMatchingService(billDatabase, transactionDatabase)
    let bogus = Bogus.Faker()

    let createUser () =
        userDatabase.CreateAsync { EmailAddress = bogus.Internet.Email(); HashedPassword = bogus.Internet.Password() }

    let createBill (userId: UserId) (name: string) (amount: decimal) (week: WeekOfMonth) (day: DayOfWeek) =
        task {
            let partialBill: PartialRecurringBill =
                { Name = name
                  Amount = amount
                  Schedule = WeekBased(week, day) }
            return! billDatabase.CreateAsync(userId, partialBill)
        }

    // Mirrors BillMatchingService.calculateTargetDateForWeek so tests can produce dates that
    // align with the bill schedule and stay within the 60-day match-candidate window.
    let calculateExpectedDate (week: WeekOfMonth) (day: DayOfWeek) =
        let now = DateTimeOffset.UtcNow
        let firstDayOfMonth = DateTimeOffset(now.Year, now.Month, 1, 0, 0, 0, TimeSpan.Zero)
        let daysUntilTargetDay = (int day - int firstDayOfMonth.DayOfWeek + 7) % 7
        let firstTargetDayOfMonth = firstDayOfMonth.AddDays(float daysUntilTargetDay)
        let weeksToAdd = week.ToInt() - 1
        firstTargetDayOfMonth.AddDays(float (weeksToAdd * 7))

    let createTransaction (userId: UserId) (name: string) (amount: decimal) (date: DateTimeOffset) =
        task {
            let partialTransaction: PartialTransaction =
                { Name = name
                  Amount = amount
                  DateCreated = date
                  Status = Pending
                  Type = Debit
                  ImportId = Some (Guid.NewGuid().ToString())
                  RecurringBillId = None
                  AutoGenerated = false }
            return! transactionDatabase.CreateAsync(userId, partialTransaction)
        }

    [<Fact>]
    member _.``Scoring should give 100 points for exact amount match``() =
        task {
            // Setup
            let! user = createUser ()
            let amount = 125.50M

            let! bill = createBill user.Id "Electric Bill" amount FirstWeek DayOfWeek.Monday
            let targetDate = calculateExpectedDate FirstWeek DayOfWeek.Monday
            let! transaction = createTransaction user.Id "ELECTRIC COMPANY" amount targetDate

            // Act
            let! matches = service.GetMatchSuggestionsForUser(user.Id)

            // Assert
            let matchesList = matches |> Seq.toList
            %matchesList.Should().NotBeEmpty()

            let match' = matchesList |> List.find (fun m -> m.Transaction.Id = transaction.Id && m.RecurringBill.Id = bill.Id)

            // Exact amount (100 * 0.45) + exact date (100 * 0.35) + no name match (0 * 0.05) + no history (0 * 0.15) = 80
            %match'.MatchScore.Should().BeGreaterThanOrEqualTo(80M)
        }

